package day02.javase.base;

public class TestDlus {
	public static void main(String[] args) {
		System.out.println("j增的循环");
		for (int j = 1; j <=5; j++) {
			System.out.println(j);	
		}
		System.out.println("j减的循环");
		for (int j = 5; j >=1; j--) {
			System.out.println(j);
			
		}
	}
}
